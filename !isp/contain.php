<script>
  // Création d'un nouvel observer pour les éléments .element
  const observer = new IntersectionObserver((entries) => {
    // Pour chaque élément entrant, vérifier si il est visible
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        // Si l'élément est visible, décompter de 0 jusqu'à sa valeur
        const dt = entry.target.querySelector("dt");
        const count = parseInt(dt.innerText);
        let currentCount = 0;
        const interval = setInterval(() => {
          if (currentCount >= count) {
            clearInterval(interval);
          }
          dt.innerText = "+" + currentCount;
          currentCount++;
        }, (1000 / count) * 2);
      }
    });
  });

  // Récupération des éléments .element et ajout de chaque élément à l'observer
  const elements = document.querySelectorAll(".contenu .element");
  elements.forEach((element) => {
    observer.observe(element);
  });
</script>
<script>
  window.addEventListener('load', function () {
    // Votre code JS ici
    // Fonction pour vérifier la visibilité d'un élément
    function isElementVisible(el) {
      var rect = el.getBoundingClientRect();
      return (rect.top >= 0 && rect.bottom <= window.innerHeight);
    }

    // Fonction pour compter de 1 à n
    function countToN(selector, n) {
      var element = document.querySelector(selector);
      var count = 0;
      var intervalId = null;

      // Fonction pour incrémenter le compteur
      function incrementCounter() {
        if (count <= n) {
          element.textContent = count;
          count++;
        } else {
          clearInterval(intervalId);
        }
      }

      // Vérifiez la visibilité toutes les 100 millisecondes
      intervalId = setInterval(function () {
        if (isElementVisible(element)) {
          incrementCounter();
        }
      }, 100);
    }

    // Compteur de 1 à 20
    countToN('#count-20 .count-20-number', 20);

    // Compteur de 1 à 60
    countToN('#count-60 .count-60-number', 60);

    // Compteur de 1 à 50
    countToN('#count-50 .count-50-number', 50);

  });


</script>



